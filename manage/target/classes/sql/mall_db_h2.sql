-- H2数据库脚本 - 商城系统

-- 用户表
CREATE TABLE IF NOT EXISTS "user" (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    username VARCHAR(50) NOT NULL,
    password VARCHAR(100) NOT NULL,
    real_name VARCHAR(50),
    phone VARCHAR(20),
    email VARCHAR(100),
    avatar VARCHAR(255),
    role TINYINT DEFAULT 2,
    status TINYINT DEFAULT 1,
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_user_username ON "user" (username);

COMMENT ON TABLE "user" IS '用户表';
COMMENT ON COLUMN "user".id IS '主键ID';
COMMENT ON COLUMN "user".username IS '用户名';
COMMENT ON COLUMN "user".password IS '密码';
COMMENT ON COLUMN "user".real_name IS '真实姓名';
COMMENT ON COLUMN "user".phone IS '手机号';
COMMENT ON COLUMN "user".email IS '邮箱';
COMMENT ON COLUMN "user".avatar IS '头像';
COMMENT ON COLUMN "user".role IS '角色：1-管理员，2-普通用户';
COMMENT ON COLUMN "user".status IS '状态：0-禁用，1-启用';
COMMENT ON COLUMN "user".create_time IS '创建时间';
COMMENT ON COLUMN "user".update_time IS '更新时间';

-- 商品分类表
CREATE TABLE IF NOT EXISTS category (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(100) NOT NULL,
    parent_id BIGINT DEFAULT 0,
    sort INT DEFAULT 0,
    status TINYINT DEFAULT 1,
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
);

COMMENT ON TABLE category IS '商品分类表';
COMMENT ON COLUMN category.id IS '主键ID';
COMMENT ON COLUMN category.name IS '分类名称';
COMMENT ON COLUMN category.parent_id IS '父分类ID';
COMMENT ON COLUMN category.sort IS '排序';
COMMENT ON COLUMN category.status IS '状态：0-禁用，1-启用';
COMMENT ON COLUMN category.create_time IS '创建时间';
COMMENT ON COLUMN category.update_time IS '更新时间';

-- 商品表
CREATE TABLE IF NOT EXISTS product (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR(200) NOT NULL,
    category_id BIGINT NOT NULL,
    price DECIMAL(10,2) NOT NULL,
    original_price DECIMAL(10,2),
    stock INT DEFAULT 0,
    image VARCHAR(255),
    description TEXT,
    status TINYINT DEFAULT 1,
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
);

CREATE INDEX IF NOT EXISTS idx_product_category_id ON product (category_id);

COMMENT ON TABLE product IS '商品表';
COMMENT ON COLUMN product.id IS '主键ID';
COMMENT ON COLUMN product.name IS '商品名称';
COMMENT ON COLUMN product.category_id IS '分类ID';
COMMENT ON COLUMN product.price IS '商品价格';
COMMENT ON COLUMN product.original_price IS '原价';
COMMENT ON COLUMN product.stock IS '库存';
COMMENT ON COLUMN product.image IS '商品图片';
COMMENT ON COLUMN product.description IS '商品描述';
COMMENT ON COLUMN product.status IS '状态：0-下架，1-上架';
COMMENT ON COLUMN product.create_time IS '创建时间';
COMMENT ON COLUMN product.update_time IS '更新时间';

-- 订单表
CREATE TABLE IF NOT EXISTS "order" (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    order_no VARCHAR(32) NOT NULL,
    user_id BIGINT NOT NULL,
    total_amount DECIMAL(10,2) NOT NULL,
    pay_amount DECIMAL(10,2) NOT NULL,
    pay_type TINYINT,
    pay_time TIMESTAMP,
    order_status TINYINT DEFAULT 1,
    consignee VARCHAR(50) NOT NULL,
    phone VARCHAR(20) NOT NULL,
    address VARCHAR(500) NOT NULL,
    remark VARCHAR(500),
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_order_order_no ON "order" (order_no);
CREATE INDEX IF NOT EXISTS idx_order_user_id ON "order" (user_id);

COMMENT ON TABLE "order" IS '订单表';
COMMENT ON COLUMN "order".id IS '主键ID';
COMMENT ON COLUMN "order".order_no IS '订单编号';
COMMENT ON COLUMN "order".user_id IS '用户ID';
COMMENT ON COLUMN "order".total_amount IS '订单总金额';
COMMENT ON COLUMN "order".pay_amount IS '实付金额';
COMMENT ON COLUMN "order".pay_type IS '支付方式：1-微信，2-支付宝';
COMMENT ON COLUMN "order".pay_time IS '支付时间';
COMMENT ON COLUMN "order".order_status IS '订单状态：1-待付款，2-待发货，3-已发货，4-已完成，5-已关闭';
COMMENT ON COLUMN "order".consignee IS '收货人';
COMMENT ON COLUMN "order".phone IS '联系电话';
COMMENT ON COLUMN "order".address IS '收货地址';
COMMENT ON COLUMN "order".remark IS '订单备注';
COMMENT ON COLUMN "order".create_time IS '创建时间';
COMMENT ON COLUMN "order".update_time IS '更新时间';

-- 订单明细表
CREATE TABLE IF NOT EXISTS order_item (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    order_id BIGINT NOT NULL,
    product_id BIGINT NOT NULL,
    product_name VARCHAR(200) NOT NULL,
    product_image VARCHAR(255),
    price DECIMAL(10,2) NOT NULL,
    quantity INT NOT NULL,
    total_amount DECIMAL(10,2) NOT NULL,
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
);

CREATE INDEX IF NOT EXISTS idx_order_item_order_id ON order_item (order_id);
CREATE INDEX IF NOT EXISTS idx_order_item_product_id ON order_item (product_id);

COMMENT ON TABLE order_item IS '订单明细表';
COMMENT ON COLUMN order_item.id IS '主键ID';
COMMENT ON COLUMN order_item.order_id IS '订单ID';
COMMENT ON COLUMN order_item.product_id IS '商品ID';
COMMENT ON COLUMN order_item.product_name IS '商品名称';
COMMENT ON COLUMN order_item.product_image IS '商品图片';
COMMENT ON COLUMN order_item.price IS '商品单价';
COMMENT ON COLUMN order_item.quantity IS '购买数量';
COMMENT ON COLUMN order_item.total_amount IS '商品总金额';
COMMENT ON COLUMN order_item.create_time IS '创建时间';

-- 物流表
CREATE TABLE IF NOT EXISTS logistics (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    order_id BIGINT NOT NULL,
    order_no VARCHAR(32) NOT NULL,
    shipping_company VARCHAR(100),
    shipping_no VARCHAR(100),
    shipping_status TINYINT DEFAULT 1,
    tracking_info TEXT,
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    update_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_logistics_order_id ON logistics (order_id);

COMMENT ON TABLE logistics IS '物流表';
COMMENT ON COLUMN logistics.id IS '主键ID';
COMMENT ON COLUMN logistics.order_id IS '订单ID';
COMMENT ON COLUMN logistics.order_no IS '订单编号';
COMMENT ON COLUMN logistics.shipping_company IS '物流公司';
COMMENT ON COLUMN logistics.shipping_no IS '物流单号';
COMMENT ON COLUMN logistics.shipping_status IS '物流状态：1-待发货，2-已发货，3-运输中，4-已签收';
COMMENT ON COLUMN logistics.tracking_info IS '物流跟踪信息';
COMMENT ON COLUMN logistics.create_time IS '创建时间';
COMMENT ON COLUMN logistics.update_time IS '更新时间';

-- 账单表
CREATE TABLE IF NOT EXISTS bill (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    bill_no VARCHAR(32) NOT NULL,
    order_id BIGINT NOT NULL,
    order_no VARCHAR(32) NOT NULL,
    user_id BIGINT NOT NULL,
    amount DECIMAL(10,2) NOT NULL,
    bill_type TINYINT NOT NULL,
    bill_status TINYINT DEFAULT 1,
    remark VARCHAR(500),
    create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY (id)
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_bill_bill_no ON bill (bill_no);
CREATE INDEX IF NOT EXISTS idx_bill_order_id ON bill (order_id);
CREATE INDEX IF NOT EXISTS idx_bill_user_id ON bill (user_id);

COMMENT ON TABLE bill IS '账单表';
COMMENT ON COLUMN bill.id IS '主键ID';
COMMENT ON COLUMN bill.bill_no IS '账单编号';
COMMENT ON COLUMN bill.order_id IS '订单ID';
COMMENT ON COLUMN bill.order_no IS '订单编号';
COMMENT ON COLUMN bill.user_id IS '用户ID';
COMMENT ON COLUMN bill.amount IS '账单金额';
COMMENT ON COLUMN bill.bill_type IS '账单类型：1-收入，2-支出';
COMMENT ON COLUMN bill.bill_status IS '账单状态：1-已确认，2-待确认';
COMMENT ON COLUMN bill.remark IS '备注';
COMMENT ON COLUMN bill.create_time IS '创建时间';

